mov_test_1:
mov al, 0xab
cmp al, 0xab
assert e, 0x0110
mov cl, 0xcd
cmp cl, 0xcd
assert e, 0x0111
mov dl, 0xbc 
cmp dl, 0xbc
assert e, 0x0112
mov bl, 0xda
cmp bl, 0xda
assert e, 0x0113
mov ah, 0xab
cmp ah, 0xab
assert e, 0x0114
mov ch, 0xcd
cmp ch, 0xcd
assert e, 0x0115
mov dh, 0xbc 
cmp dh, 0xbc
assert e, 0x0116
mov bh, 0xda
cmp bh, 0xda
assert e, 0x0117
mov ax, 0xabcd
cmp ax, 0xabcd
assert e, 0x0118
mov cx, 0xdabc
cmp cx, 0xdabc
assert e, 0x0119
mov dx, 0xcdab
cmp dx, 0xcdab
assert e, 0x011a
mov bx, 0xbcda
cmp bx, 0xbcda
assert e, 0x011b
mov sp, 0xdcba
cmp sp, 0xdcba
assert e, 0x011c
mov bp, 0xadcb
cmp bp, 0xadcb
assert e, 0x011d
mov si, 0xbadc
cmp si, 0xbadc
assert e, 0x011e
mov di, 0xcbad
cmp di, 0xcbad
assert e, 0x011f
