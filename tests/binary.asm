; Test set 0x0400
binary_test_1:
; Test ADD / ADC
clc
mov ax, 0x0001
add ax, 0x0001
adc ax, 0x0001
cmp ax, 0x0003
assert e, 0x0400
stc
adc ax, 0x0001
cmp ax, 0x0005
assert e, 0x0401
; Test CF true
mov ax, 0xfff0
add ax, 0x0010
assert c, 0x0402
; Test CF false
mov ax, 0x8000
add ax, 0x1000
assert nc, 0x0403
; Test PF
mov ax, 0x0000
add ax, 0x1111
assert p, 0x0404
add ax, 0x0001
assert p, 0x0405
add ax, 0x0001
assert np, 0x0406
; Test AF
mov bx, 0x0006
add bx, 0x0001
lahf
test ah, 0x0010
assert z, 0x0407
add bx, 0x0007
lahf
test ah, 0x0010
assert nz, 0x0408
; Test ZF
mov ax, 0x0000
add ax, 0x0000
assert z, 0x0409
add ax, 0x0001
assert nz, 0x040a
; Test SF
mov ax, 3
add ax, -4
assert s, 0x040b
add ax, 6
assert ns, 0x040c
; Test OF
mov ax, 0x7fff
add ax, 0x0001
assert o, 0x040d
add ax, 0x0002
assert no, 0x040e
